public class Indirizzo {

    private int cap, civico;
    private String via;

    public void setCap(int cap) {
        this.cap = cap;
    }

    public void setCivico(int civico) {
        this.civico = civico;
    }

    public void setVia(String via) {
        this.via = via;
    }

    public int getCap() {
        return cap;
    }

    public int getCivico() {
        return civico;
    }

    public String getVia() {
        return via;
    }
}
