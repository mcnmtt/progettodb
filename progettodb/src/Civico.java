public class Civico {

    private int idCivico, numero, cap;

    public int getIdCivico() {
        return idCivico;
    }

    public void setIdCivico(int idCivico) {
        this.idCivico = idCivico;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public int getCap() {
        return cap;
    }

    public void setCap(int cap) {
        this.cap = cap;
    }
}
